    private void criaBanco() throws SQLException {
        String usuario = tfUser.getText();
        String senha = tfPass.getText();

        HibernateUtil.setUser(usuario);
        HibernateUtil.setPass(senha);

        Connection conexao = Conexao.getConnection();
        if (Util.imprimirConfirmacao("Já possui a base de dados ?")) {
            Util.abrirJFrameCentralizado(new JFrameHome());
            this.dispose();
        } else {
            try {
                conexao.createStatement().execute("create database syshotel");
              //  conexao.createStatement().execute("use syshotel");
                HibernateUtil.criarSchema();
                Util.abrirJFrameCentralizado(new JFrameHome());
                this.dispose();
            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Problema ao criar Banco");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro inesperado ao tentar iniciar");
            }
        }

    }
