    private void obterPorNome() {
        try {
            model = new DefaultTableModel();
            model.setColumnIdentifiers(dadosMunicipio);
            municipios = new DaoMunicipio().obterNome(tfNome.getText());
            prencherTabela();
        } catch (Exception e) {
            System.out.println("Erro ao procurar por nome!");
            novo();
        }

    }

    private void novo() {
        tfNome.setText(null);
        popularTabela();
    }

    private void popularTabela() {
        municipios = new DaoMunicipio().listar();
        model = new DefaultTableModel();
        model.setColumnIdentifiers(dadosMunicipio);
        prencherTabela();

    }

    private void prencherTabela() {
        if (municipios != null && !municipios.isEmpty()) {

            for (Municipio city : municipios) {
                model.addRow(new Object[]{city.getId(),
                    city.getNome(),});

            }
            tbMunicipio.setModel(model);
        } else {
            model = new DefaultTableModel();
            model.setColumnIdentifiers(dadosMunicipio);
            tbMunicipio.setModel(model);
        }
    }

    private void atualizar() {
        if (tbMunicipio.getSelectedRow() != -1) {
            if (Util.imprimirConfirmacao("Atualizar")) {
                int linha = tbMunicipio.getSelectedRow();
                if (linha == -1) {
                    return;
                }
                Data.hash.put("cidade", municipios.get(linha));
            }
        }

        dispose();
    }
